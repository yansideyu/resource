const getOrderGoodsInfo = (order) => {
	try {
		const goodsInfo = order.goodsList.map((item) => ({
			...item,
			goods: JSON.parse(item.goodsSpecification),
		}))
		return goodsInfo.map((item) => {
			const last = item.goods.slice(-1)[0]
			return { label: last.value, goodsQuantity: item.goodsQuantity, originalPrice: item.originalPrice }
		})
	} catch (e) {
		return []
	}
}

