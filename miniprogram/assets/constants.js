const getOrderGoodsTxtArr = (order) => {
	try {
		const goodsInfo = order.goodsList.map((item) => ({
			...item,
			goods: JSON.parse(item.goodsSpecification),
		}))
		const single = goodsInfo[0]
		const singleType = single.goods.slice(0, -1).map((item) => item.value).join(' · ')
		const doubleType = goodsInfo.map((item) => {
			const last = item.goods.slice(-1)[0].value
			return `${last} × ${item.goodsQuantity}`
		}).join(' · ')
		return [singleType, doubleType]
	} catch (e) {
		return []
	}
}

