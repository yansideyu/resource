const getOrderStatus = (order) => {
	// activity 属性 就是 活动详情接口返回的所有数据
	const {
		orderStatus,
		refundStatus,
		activity: { expireFlag },
	} = order
	const isChecked = orderStatus === 3 || orderStatus === 4 // 已核销
	const isExpireAndUnCheck = orderStatus === 2 && !expireFlag // 待核销 + 活动已过期
	const isCheckingRefund = refundStatus === 1 // 退款审核中
	const isRefunding = refundStatus === 2 || refundStatus === 3 || refundStatus === 4 // 退款中
	const isRefunded = refundStatus === 6 // 已退款
	const isEnd = refundStatus === 5 // 已结束 [退款失败]
	return { isChecked, isExpireAndUnCheck, isCheckingRefund, isRefunding, isRefunded, isEnd }
}

// expireFlag 在活动结束前
const orderStatusTxt = (order) => {
	const {
		refundStatus,
		orderStatus,
		activity: { expireFlag },
	} = order
	const { isChecked, isExpireAndUnCheck, isCheckingRefund, isRefunding, isRefunded, isEnd } = getOrderStatus(order)
	if (!refundStatus || refundStatus === 7 || refundStatus === 5) {
		// 无售后状态 (未发起过退款)
		if (!expireFlag && orderStatus === 2) return '已结束' // 活动过期 但是 未参与(待核销)
		if (isExpireAndUnCheck || isChecked) return '已完成'
		if (orderStatus === 2) return '待参与'
		if (orderStatus === 1) return '待付款'
		if (orderStatus === 5) return '交易关闭'
		return '已完成' // 未知
	} else if (isCheckingRefund) {
		return '退款审核'
	} else if (isRefunding) {
		return '退款中'
	} else if (isRefunded) {
		return '已退款'
	} else if (isEnd) {
		return '已结束' // 严格来说应该是 退款失败
	} else {
		return '已完成' // 未知
	}
}

const orderPayDetailTxt = (order) => {
	const { orderStatus, refundData } = order
	const isCheckingRefund = refundData && refundData.refundStatus !== 5 && refundData.refundStatus !== 6 && refundData.refundStatus !== 7
	const isRefunded = refundData && refundData.refundStatus === 6
	const isPaySuccess = orderStatus === 2 || orderStatus === 3 || orderStatus === 4
	const isToPay = orderStatus === 1
	const isClosed = orderStatus === 5
	if (refundData) {
		if (isCheckingRefund) return '退款审核中'
		if (isRefunded) return '已退款'
		if (refundData.refundStatus === 5 || refundData.refundStatus === 7) return '支付成功'
	}
	if (isToPay) return '待支付'
	if (isPaySuccess) return '支付成功'
	if (isClosed) return '订单已关闭'
	return '支付成功' // 未知
}

